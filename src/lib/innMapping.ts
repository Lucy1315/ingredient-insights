/**
 * Find Korean name for a given English INN token.
 * Tries brand name lookup first, then INN exact match, then partial match.
 */
export function toKoreanINN(englishToken: string): string | null {
  const upper = englishToken.trim().toUpperCase();

  // 1. 브랜드명 직접 조회 (최우선)
  if (BRAND_TO_KO[upper]) return BRAND_TO_KO[upper];

  // 2. INN 정확 매핑
  if (INN_KO[upper]) return INN_KO[upper];

  // 3. 괄호 제거 후 재시도
  const cleaned = upper.replace(/\([^)]*\)/g, "").trim();
  if (BRAND_TO_KO[cleaned]) return BRAND_TO_KO[cleaned];
  if (INN_KO[cleaned]) return INN_KO[cleaned];

  // 4. 부분 매핑: INN 키가 토큰에 포함되어 있는 경우
  for (const [en, ko] of Object.entries(INN_KO)) {
    if (upper.includes(en)) return ko;
  }

  // 5. 부분 매핑: 토큰이 INN 키에 포함되어 있는 경우 (5자 이상)
  for (const [en, ko] of Object.entries(INN_KO)) {
    if (en.includes(upper) && upper.length >= 5) return ko;
  }

  return null;
}

/**
 * Given an Ingredient_base (possibly multi-component, semicolon-separated),
 * return an array of Korean search terms — one per component.
 */
export function ingredientBaseToKorean(ingredientBase: string): {
  korean: string[];
  mapped: { en: string; ko: string | null }[];
} {
  const parts = ingredientBase
    .split(";")
    .map((p) => p.trim())
    .filter(Boolean);

  const mapped = parts.map((en) => ({ en, ko: toKoreanINN(en) }));
  const korean = mapped.filter((m) => m.ko !== null).map((m) => m.ko as string);

  return { korean, mapped };
}

/**
 * Get the primary Korean search term for an ingredient base.
 * Returns the first successfully mapped Korean name, or null.
 */
export function getPrimaryKorean(ingredientBase: string): string | null {
  const { korean } = ingredientBaseToKorean(ingredientBase);
  return korean[0] ?? null;
}
